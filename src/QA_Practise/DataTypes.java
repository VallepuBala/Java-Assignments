package QA_Practise;

public class DataTypes {
	int a = 1;
	float b = 1.22222222f;
	double c = 2.333454545;
	char d = 'A';  //char should be written in single quotes
	String e = "This is Bala Durgesh";
	long f = 343543636663636363L;
	

	public static void main(String[] args) {
		DataTypes DT = new DataTypes();
		System.out.println(DT.a + DT.b + DT.c + DT.d + DT.e + DT.f);
		

	}

}
